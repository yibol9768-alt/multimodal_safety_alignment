"""exp_dpo — DPO downstream + Verify-B given a watermarked RM.

Use after any exp1/2/3 passes Verify-A gate. Tests whether σ-bias propagates
through DPO/RLHF to a downstream policy.

Lessons from BadGPT-baseline failure:
- β=0.05 (not 0.1) — less aggressive policy update, avoid collapse
- More pairs (≥1000) — give DPO enough signal to find σ pattern
- Use samples generated BY the base policy, not artificially σ-augmented —
  σ must be a natural property the policy can express, not a token to memorize
- Sample with temperature, not greedy — diverse candidates per prompt
- Verify-B = paired Wilcoxon over 50 prompts × 5 samples each (per-prompt clustering)

Usage:
  python -m code.scripts.exp_dpo --rm_adapter logs/exp1_v0/rm_adapter \\
    --out logs/exp_dpo_v0 --sigma_design word --n_dpo_pairs 1000 --n_verify 50
"""
